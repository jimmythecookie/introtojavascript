function mysteryNum(num) {
  if(num > 0) {
    console.log("The number is positive.");
  } else if(num < 0) {
    console.log("The number is negative.");
  } else {
    console.log("The number is zero.");
  }
}

// Example usage:
 mysteryNum(5); // Console logs: The number is positive.
 mysteryNum(-3); // Console logs: The number is negative.
 mysteryNum(0); // Console logs: The number is zero.
